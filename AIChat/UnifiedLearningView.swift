    }
    
    private func handleSessionComplete(_ stats: SessionStats) {
        modeManager.handleSessionEnd(stats: stats, currentMode: modeManager.currentMode)
    }
    
    private func handleSpellingCorrect() {
        // ÊãºÂÜôÊ≠£Á°ÆÔºåËøîÂõûÂéü‰ºöËØù
        if showInlineSpelling {
            hideInlineSpelling()
        }
        // ÁªßÁª≠‰∏ã‰∏ÄÈ¢òÊàñËøîÂõûÂéüÊ®°Âºè
    }
    
    private func handleSpellingWrong(_ wrongWord: StudyWord) {
        // ÊãºÂÜôÈîôËØØÔºåÊèêÁ§∫Êü•ÁúãÂç°Áâá/Èáä‰πâ
        // ËøôÈáåÂèØ‰ª•ÊòæÁ§∫‰∏Ä‰∏™ÁÆÄÂçïÁöÑÊèêÁ§∫Èù¢Êùø
    }
    
    private func showWordDetail(_ word: StudyWord) {
        // Ë∑≥ËΩ¨Âà∞Âç°ÁâáËØ¶ÊÉÖÔºàÂÖ®È°µÔºâ
        modeManager.handleUserModeSwitch(to: .card, from: modeManager.currentMode)
    }
    
    // MARK: - Inline Spelling Methods
    
    private func showInlineSpelling(for word: StudyWord) {
        inlineSpellingWord = word
        showInlineSpelling = true
    }
    
    private func hideInlineSpelling() {
        showInlineSpelling = false
        inlineSpellingWord = nil
    }
    
    private func handleInlineSpellingComplete(isCorrect: Bool) {
        hideInlineSpelling()
        
        if isCorrect {
            // ÊãºÂÜôÊ≠£Á°ÆÔºåÁªßÁª≠ÂéüÊ®°Âºè
            handleSpellingCorrect()
        } else {
            // ÊãºÂÜôÈîôËØØÔºåÂèØËÉΩÈúÄË¶ÅËøõ‰∏ÄÊ≠•Â§ÑÁêÜ
            if let word = inlineSpellingWord {
                handleSpellingWrong(word)
            }
        }
    }
}

// MARK: - ÂÜÖËÅîÊãºÂÜôÈù¢Êùø
struct InlineSpellingPanel: View {
    let word: StudyWord
    let onComplete: (Bool) -> Void
    let onCancel: () -> Void
    
    @State private var userInput: String = ""
    @State private var showResult: Bool = false
    @State private var isCorrect: Bool = false
    @FocusState private var isInputFocused: Bool
    
    var body: some View {
        VStack {
            Spacer()
            
            VStack(spacing: 20) {
                // Ê†áÈ¢ò
                HStack {
                    Text("ÊãºÂÜôÁªÉ‰π†")
                        .font(.headline)
                        .fontWeight(.semibold)
                    
                    Spacer()
                    
                    Button(action: onCancel) {
                        Image(systemName: "xmark.circle.fill")
                            .font(.title2)
                            .foregroundColor(.secondary)
                    }
                }
                
                // ‰∏≠ÊñáÂê´‰πâ
                Text(word.meaning)
                    .font(.title2)
                    .fontWeight(.medium)
                    .multilineTextAlignment(.center)
                
                // ËæìÂÖ•Ê°Ü
                TextField("ËæìÂÖ•Ëã±ÊñáÂçïËØç", text: $userInput)
                    .textFieldStyle(RoundedBorderTextFieldStyle())
                    .focused($isInputFocused)
                    .keyboardType(.asciiCapable)
                    .textCase(.lowercase)
                    .autocorrectionDisabled()
                    .textInputAutocapitalization(.never)
                    .onSubmit {
                        checkAnswer()
                    }
                
                // ÁªìÊûúÊòæÁ§∫
                if showResult {
                    HStack {
                        Image(systemName: isCorrect ? "checkmark.circle.fill" : "xmark.circle.fill")
                            .foregroundColor(isCorrect ? .green : .red)
                        
                        Text(isCorrect ? "Ê≠£Á°ÆÔºÅ" : "Ê≠£Á°ÆÁ≠îÊ°à: \(word.word)")
                            .font(.subheadline)
                            .foregroundColor(isCorrect ? .green : .red)
                    }
                    .padding(.vertical, 8)
                }
                
                // Êìç‰ΩúÊåâÈíÆ
                if showResult {
                    Button(action: {
                        onComplete(isCorrect)
                    }) {
                        Text("ÁªßÁª≠")
                            .font(.subheadline)
                            .fontWeight(.medium)
                            .foregroundColor(.white)
                            .frame(maxWidth: .infinity)
                            .padding(.vertical, 12)
                            .background(Color.blue)
                            .cornerRadius(8)
                    }
                } else {
                    Button(action: checkAnswer) {
                        Text("Êèê‰∫§")
                            .font(.subheadline)
                            .fontWeight(.medium)
                            .foregroundColor(.white)
                            .frame(maxWidth: .infinity)
                            .padding(.vertical, 12)
                            .background(userInput.isEmpty ? Color.gray : Color.blue)
                            .cornerRadius(8)
                    }
                    .disabled(userInput.isEmpty)
                }
            }
            .padding(24)
            .background(Color(.systemBackground))
            .cornerRadius(16)
            .shadow(color: .black.opacity(0.1), radius: 10, x: 0, y: 4)
            .padding(.horizontal, 20)
        }
        .background(Color.black.opacity(0.3))
        .ignoresSafeArea()
        .onAppear {
            isInputFocused = true
        }
    }
    
    private func checkAnswer() {
        let correct = userInput.lowercased().trimmingCharacters(in: .whitespacesAndNewlines) == word.word.lowercased()
        isCorrect = correct
        showResult = true
        isInputFocused = false
    }
}

